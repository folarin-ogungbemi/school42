#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;

	if (fd < 0)
		return ;
	if (n == INT_MIN)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 0)
	{
		if (n > 9)
			ft_putnbr_fd((n / 10), fd);
		digit = (n % 10) + 48;
		write(fd, &digit, 1);
	}
}
